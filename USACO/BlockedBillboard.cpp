#include <bits/stdc++.h>

using namespace std;

void setIO(string s, int inputFromFile) {
  if (inputFromFile) {
    freopen((s + ".in").c_str(), "r", stdin);
    freopen((s + ".out").c_str(), "w", stdout);
  }
}

int main() {
  setIO("billboard", 0);
  int b1_x, b1_y, b2_x, b2_y, t_x, t_y;
  cin >> b1_x >> b1_y >> b2_x >> b2_y >> t_x >> t_y;


  return 0;
}
